# Operadores Aritméticos
# + (Adição)
# - (Subtração)
# * (Multiplicação)
# / (Divisão)
# // (Divisão inteira)
# % (Resto da divisão)
# ** (Potência)

# Exemplos:
# soma = 1 + 2
# subtracao = 3 - 1
# multiplicacao = 2 * 2
# divisao = 4 / 2
# divisao_inteira = 5 // 2
# resto = 5 % 2
# potencia = 2 ** 3

# -------------------------------------------------------------------------------- 

# Exemplos em código

num1 = 100
num2 = 50

soma = num1 + num2
subtracao = num1 - num2
multiplicacao = num1 * num2
divisao = num1 / num2
divisao_inteira = num1 // num2
resto = num1 % num2
potencia = num1 ** num2

# Printando os resultados
print("Soma: ", soma)
print("Subtração: ", subtracao)
print("Multiplicação: ", multiplicacao)
print("Divisão: ", divisao)
print("Divisão inteira: ", divisao_inteira)
print("Resto: ", resto)
print("Potência: ", potencia)